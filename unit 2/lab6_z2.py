number1 = int(input("Podaj pierwszą liczbe całkowitą: "))
number2 = int(input("Podaj drugą liczbe całkowitą: "))
sum = number1 + number2
roz = number1 - number2
mult = number1 * number2
dziel = number1 / number2
modu = number1 % number2
print("Wynik dodawania ", sum, "Wynik odejmowania ", roz, "Wynik mnożenia ", mult)
print("Wynik dzielenia ", dziel, "Wynik modulo ", modu)